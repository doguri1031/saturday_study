package day1;

public class ArithmeticOperator {

	public static void main(String[] args) {
		
		int result;
		double resultDouble;
		
		result = 3 + 2;
		System.out.println(result);
		result = 3 - 2;
		System.out.println(result);
		result = 3 * 2;
		System.out.println(result);
		result = 3 / 2;
		System.out.println(result);
		resultDouble = 3.0 / 2.0;
		System.out.println(resultDouble);
		result = 8 % 3;
		System.out.println(result);
		


	}

}
