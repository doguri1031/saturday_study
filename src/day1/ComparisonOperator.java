package day1;

public class ComparisonOperator {

	public static void main(String[] args) {

		int x = 3;
		int y = 4;
		
		System.out.println(x == y); // false
		System.out.println(x != y); // true 
		System.out.println(x > y); // false
		System.out.println(x < y); // true
		System.out.println(x <= y); // true
	}

}
